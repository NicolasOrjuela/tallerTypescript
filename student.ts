export class Student {
    name: string;
    code: string;
    id: string;
    age: number;
    address: string;
    telephone: string;
  
    constructor(name: string, code: string, id: string, age: number, address: string, telephone: string) {
      this.name = name;
      this.code = code;
      this.id = id;
      this.age = age;
      this.address = address;
      this.telephone = telephone;
    }
  }